    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        TelaConfirm confirmar = new TelaConfirm(this);
        Dados dadosUser = new Dados();
        
        dadosUser.setNome(txtNome.getText());
        dadosUser.setSenha(txtSenha.getText());
        dadosUser.setCPF(txtCPF.getText());
        dadosUser.setCidade(txtCidade.getText());
        dadosUser.setEstado(txtEstado.getText());
        dadosUser.setRua(txtRua.getText());
        
        if(!dadosUser.getNome().isEmpty() && !dadosUser.getSenha().isEmpty() && !dadosUser.getCPF().isEmpty()
                && !dadosUser.getCidade().isEmpty() && !dadosUser.getEstado().isEmpty() && !dadosUser.getRua().isEmpty()){
            confirmar.setVisible(true);
            confirmar.getDados(dadosUser);
        } else {
            JOptionPane.showMessageDialog(null, "ERRO! Falta de dados!", "ERRO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnCadActionPerformed

